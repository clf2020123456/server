package entity;

public class WeekAnalysis {


    private String date;

    private String manNum;


    private String womanNum;


    private String carType;

    private String carDaxiao;

    private String type;


    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getManNum() {
        return manNum;
    }

    public void setManNum(String manNum) {
        this.manNum = manNum;
    }

    public String getWomanNum() {
        return womanNum;
    }

    public void setWomanNum(String womanNum) {
        this.womanNum = womanNum;
    }

    public String getCarType() {
        return carType;
    }

    public void setCarType(String carType) {
        this.carType = carType;
    }

    public String getCarDaxiao() {
        return carDaxiao;
    }

    public void setCarDaxiao(String carDaxiao) {
        this.carDaxiao = carDaxiao;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
